    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String name;
        double price;
        boolean pendingPayment;
        boolean available;
        String availableStylist;
        name = txtName.getText();
        price = Double.parseDouble(txtPrice.getText());
        available = Boolean.valueOf(txtAvailable.getText());
        availableStylist = comboBoxStylist.getSelectedItem().toString();
        Service service = new Service(name, price, available, availableStylist);

        serviceController.create(serviceController.buildDocument(service));

        Document result = serviceController.read(serviceController.buildDocument(service));
        if (result != null) {
            JOptionPane.showMessageDialog(null, "Successfully created");
        } else {
            JOptionPane.showMessageDialog(null, "A problem has occurred");
        }
    }//GEN-LAST:event_btnAddActionPerformed

