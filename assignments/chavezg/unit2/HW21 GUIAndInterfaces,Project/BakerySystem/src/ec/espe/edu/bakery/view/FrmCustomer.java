    public void readFromInput(){
        int id;
        String name;
        float totalSales;
        id = Integer.parseInt(txtId.getText());
        name = txtName.getText();
        totalSales = Float.parseFloat(txtTotalSales.getText());
        
        customer = new Customer(id , name, totalSales);
    }
    
