    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Building building;
        MongoDBManager saveBuilding;
        
        building = new Building();
        saveBuilding = new MongoDBManager();
        
      
        building.setTypeOfBuilding(txtTypeBuilding.getText());
        building.setMaterial(txtMaterial.getText());
        building.setSize(JTValidation(txtSize.getText()));
        building.setNumberOfFloors(JTValidation(txtFloor.getText()));
        building.setCost(JTValidation(txtCost.getText()));
        
        
        saveBuilding.CreateBuilding(building);
       JOptionPane.showMessageDialog(null, "Building added succesfull");
    }//GEN-LAST:event_jButton1ActionPerformed
      
    
    public int JTValidation(String Age) {
    
        InputNumbersValidation input;
        input= new InputNumbersValidation();
        int validateInput = 0;
        
        boolean inputJT;
        inputJT= input.ValidationNumbers(Age);
       
        if(inputJT!=false){
            
            validateInput= Integer.parseInt(Age);
                    
        }else{
            JOptionPane.showMessageDialog(null, "La ruta o distancia debe ser un numero");
        }
        return validateInput;
        
        
    }
