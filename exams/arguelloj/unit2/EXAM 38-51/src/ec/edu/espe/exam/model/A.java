public class A {
    private int aNum;
    private String aName;

    public A(int aNum, String aName) {
        this.aNum = aNum;
        this.aName = aName;
    }

    /**
     * @return the aNum
     */
    public int getaNum() {
        return aNum;
    }

    /**
     * @param aNum the aNum to set
     */
    public void setaNum(int aNum) {
        this.aNum = aNum;
    }

    /**
     * @return the aName
     */
    public String getaName() {
        return aName;
    }

    /**
     * @param aName the aName to set
     */
    public void setaName(String aName) {
        this.aName = aName;
    }
    
}
