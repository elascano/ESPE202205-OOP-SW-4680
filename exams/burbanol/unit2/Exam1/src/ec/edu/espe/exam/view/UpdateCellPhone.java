    private void jBtnUpdateCellPhoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateCellPhoneActionPerformed
        MongoCollection<Document> collection = ConnectionDatabase.database.getCollection("cellphones");
        Document doc = collection.find(eq("serialNumber", Integer.parseInt(jtxtSerialFind.getText()))).first();
        CellPhone cellPhone = new CellPhone(Integer.parseInt(jTxtSerialNumber.getText()), jTxtModel.getText(), jTxtBrand.getText(), Integer.parseInt(jTxtRam.getText()), Integer.parseInt(jTxtStorage.getText()), Double.parseDouble(jTxtPrice.getText()));
        CellphoneController cellphoneController = new CellphoneController("cellphones");
        cellphoneController.uploadToDatabase(doc, cellPhone.builDocument());
    }//GEN-LAST:event_jBtnUpdateCellPhoneActionPerformed

