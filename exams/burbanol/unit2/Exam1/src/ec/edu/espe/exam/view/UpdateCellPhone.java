    private void jFindCellPhoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFindCellPhoneActionPerformed

        
        CellphoneController cellphoneController;
        cellphoneController = new CellphoneController("cellphones");
        MongoCollection<Document> collection = ConnectionDatabase.database.getCollection("cellphones");
        
        
        Document doc = collection.find(eq("serialNumber", Integer.parseInt(jtxtSerialFind.getText()))).first();
        System.out.println(doc.toJson());
        
        
        Gson gson = new Gson();
        CellPhone cellphone = gson.fromJson(doc.toJson(), CellPhone.class);
        jTxtBrand.setText(cellphone.getBrand());
        jTxtModel.setText(cellphone.getModel());
        jTxtRam.setText(String.valueOf(cellphone.getRam()));
        jTxtStorage.setText(String.valueOf(cellphone.getStorage()));
        jTxtSerialNumber.setText(String.valueOf(cellphone.getSerialNumber()));
        jTxtPrice.setText(String.valueOf(cellphone.getPrice()));
        
        
    }//GEN-LAST:event_jFindCellPhoneActionPerformed

