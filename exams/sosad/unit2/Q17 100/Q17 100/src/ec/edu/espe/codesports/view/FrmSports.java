    public static DBObject createDBObject(Sports sports)
    {
        BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
        docBuilder.append("code", sports.getCode());
        docBuilder.append("name", sports.getName());
        docBuilder.append("type", sports.getType());
        docBuilder.append("description", sports.getDescription());
        docBuilder.append("numberOfparticipants", sports.getNumberOfparticipants());
        docBuilder.append("typeOfparticipants", sports.getTypeOfparticipants());
        return docBuilder.get();
    }
