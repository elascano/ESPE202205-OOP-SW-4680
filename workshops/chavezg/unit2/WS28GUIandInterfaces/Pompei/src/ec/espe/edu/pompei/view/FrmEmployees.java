    public void populateEmployeesTable() {
        ArrayList<Employee> employees;
        employees = new ArrayList<>();
//read from database
        Employee employee = new Employee("Genaro", "Chavez", "gchavez@gmail.com", "***");
        employees.add(employee);
        Employee employee2 = new Employee("Mateo", "Condor", "mat_condor@gmail.com", "***");
        employees.add(employee2);
        Employee employee3 = new Employee("Daniel", "Chiri", "dani_@gmail.com", "***");
        employees.add(employee3);

        Object[][] objects = new Object[employees.size()][6];

        for (int i = 0; i < employees.size(); i++) {
            objects[i][0] = i;
            objects[i][1] = employees.get(i).getName();
            objects[i][2] = employees.get(i).getLastName();
            objects[i][3] = employees.get(i).getEmail();
            objects[i][4] = employees.get(i).getPassword();
            objects[i][5] = "400";
       

            tblEmployees.setModel(new javax.swing.table.DefaultTableModel(
                    objects,
                    new String[]{
                        "Id", "Name", "Last Name", "Email", "Password","Sueldo"

                    }
            ));
        }

    }

