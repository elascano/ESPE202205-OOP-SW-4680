    public void readFromInput() {
        int id;
        String name;
        Float price;
        id = Integer.parseInt(txtId.getText());
        name = txtName.getText();
        price = Float.parseFloat(txtPrice.getText());

        customer = new Customer(id, name, price);

        JOptionPane.showConfirmDialog(this, "Tos datos: ");
    }

