public void populateCustomersTable() {
        ArrayList<User> users;
        users = new ArrayList<>();
        User user;

        //read from database
        user = new User(123, "Juanito", 89887879);
        users.add(user);
        user = new User(125, "Steven", 1231231);
        users.add(user);
        user = new User(124, "Raul", 23123414);
        users.add(user);
        user = new User(126, "Daniela", 341243);
        users.add(user);        
        Object[][] objects = new Object[users.size()][4];

        for (int i = 0; i < users.size(); i++) {
            objects[i][0] = users.get(i).getId();
            objects[i][1] = users.get(i).getName();
            objects[i][2] = users.get(i).getCellPhone();


            tblUsers.setModel(new javax.swing.table.DefaultTableModel(
                    objects,
                    new String[]{
                        "Id", "Name", "Phone Number"
                    }
            ));

        }

    }
