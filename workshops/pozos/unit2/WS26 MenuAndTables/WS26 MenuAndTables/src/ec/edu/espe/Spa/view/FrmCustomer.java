    private void txtIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKeyPressed
        String value = txtID.getText();
        int legth = value.length(); //saca la longitud del string
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            txtID.setEditable(true);
            if(legth>=10){
              
                JOptionPane.showMessageDialog(null, "ID must have 10 digits","Warning",JOptionPane.WARNING_MESSAGE);
                txtID.setText("");
                
            }
        }else {
            JOptionPane.showMessageDialog(null, "Enter only numbers","Warning",JOptionPane.WARNING_MESSAGE);
                txtID.setText("");
        }
        
    }//GEN-LAST:event_txtIDKeyPressed

