    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        Customer customer;
        int id;
        String FullName;
        String email;
        String type;
        float discount;
        float totalSale=0.0F;
        CustomerController customerController;
        customerController=new CustomerController();
        
        id=Integer.parseInt(txtId.getText());
        FullName=txtFullName.getText();
        email=txtEmail.getText();
        type=cmbCustomerType.getSelectedItem().toString();
        discount=Float.parseFloat(spnDiscount.getValue().toString());
        
        customer= new Customer(id,FullName,email,type,discount,totalSale);
        
        customerController.register(customer,this);
    }//GEN-LAST:event_btnRegisterActionPerformed

