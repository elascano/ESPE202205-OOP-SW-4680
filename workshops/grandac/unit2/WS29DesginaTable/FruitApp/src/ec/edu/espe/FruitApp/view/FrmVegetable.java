    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        Vegetable vegetable;
        String name;
        String texture;
        int quantity;
        double weight;
        float cost;
        
        VegetableController vegetableController;
        vegetableController = new VegetableController();
        
        name = cmbName.getSelectedItem().toString();
        texture = cmbTexture.getSelectedItem().toString();
        quantity = Integer.parseInt(spnQuantity.getValue().toString());
        weight = Double.parseDouble(txtWeight.getText());
        cost = Float.parseFloat(txtCost.getText());
        
        vegetable = new Vegetable(name, texture, quantity, weight, cost);
        
        vegetableController.register(vegetable, this);
        
         ConexionMongoDBVerdure conexionMongoDb = new ConexionMongoDBVerdure();
        conexionMongoDb.ConexionVerdure(name, texture, quantity, weight, cost);
    }//GEN-LAST:event_btnAddActionPerformed

