    private void jRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRegisterActionPerformed
       
        String names;
        String surnames;
        String cellphone;
        String ID;
        String email;
        String typeService;
        String date;
        String price;

        MongoDBConnection mongoDB;
        mongoDB = new MongoDBConnection();
        MongoClient userDB;
        MongoDatabase userdatabase;
        MongoCollection userCollection;
        Document document;

        userDB= mongoDB.conectionMongo();
        userdatabase=userDB.getDatabase("LeatherFlowerSystem");
        userCollection= userdatabase.getCollection("Customer");

        document=new Document();

        typeService = txtType.getText();
        document.append("Type of Service",typeService);

        price = txtPrice.getText();
        document.append("Price",price);

        date = txtDate.getText();
        document.append("Date of Appointment",date);

        names=txtName.getText();
        document.append("Full Names",names);

        surnames=txtSurnames.getText();
        document.append("Surnames",surnames);

        cellphone=txtCellphone.getText();
        document.append("Cellphone",cellphone);

        ID=txtID.getText();
        document.append("ID",ID);

        email=txtEmail.getText();
        document.append("Email",email);

        userCollection.insertOne(document);
        JOptionPane.showMessageDialog(null, "The data has been saved successfully");
    }//GEN-LAST:event_jRegisterActionPerformed

