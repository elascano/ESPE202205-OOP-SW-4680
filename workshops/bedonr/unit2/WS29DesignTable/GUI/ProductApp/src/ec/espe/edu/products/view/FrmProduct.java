    private void txtPriceKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPriceKeyPressed
        String value = txtPrice.getText();   //cada que alguien pone letra pongo esoo
        int length = value.length();  //Saca la longitud del String
        if(evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9' ){
            txtPrice.setEditable(true);
            lblpriceError.setText("");
            if(length>8){
            txtPrice.setEditable(false);
            lblpriceError.setText("Max 8 digital");
            }
               
            
        }else{
            txtPrice.setEditable(false);
            lblpriceError.setText("* Enter only digital");
        }
    }//GEN-LAST:event_txtPriceKeyPressed

