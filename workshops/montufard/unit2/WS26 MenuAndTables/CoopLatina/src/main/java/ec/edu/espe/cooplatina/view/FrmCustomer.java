    public void readFromInput() {
        int id;
        String name;
        float cellphone;
        id = Integer.parseInt(txtId.getText());
        name = txtName.getText();
        cellphone = Float.parseFloat(txtCellphone.getText());

        customer = new Customer(id, name, cellphone);
    }

